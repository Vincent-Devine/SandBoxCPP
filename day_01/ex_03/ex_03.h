namespace day_01::ex_03
{
    int add(int a, int b)
    {
        return (a + b);
    }

    float add(float a, float b)
    {
        return (a + b);
    }

    double add(double a, double b)
    {
        return (a + b);
    }

    unsigned int add(unsigned int a, unsigned int b)
    {
        return (a + b);
    }

    double add(double a, unsigned int b = 0)
    {
        return (a + b);
    }
}